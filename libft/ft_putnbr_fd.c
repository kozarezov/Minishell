#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char ch;

	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * (-1);
		}
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ch = n % 10 + '0';
	write(fd, &ch, 1);
}
